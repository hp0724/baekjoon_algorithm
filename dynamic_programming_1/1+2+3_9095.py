t_c = int(input())

dp = [0] * 11
dp[1] = 1
dp[2] = 2
dp[3] = 4

for i in range(4, 11):
    dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3]
# 1 = (1)      1= 1
# 2 = (1,1) (2)   1+1 = 2
# 3= (1,1,1) (1,2) (3)  1 +2 + 1 = 4
# 4 = (1,1,1,1) (1,1,2) (1,3) (2,2) 1+3+2+1 =7
# 5 = (1,1,1,1,1) ,(1,1,1,2) (1,1,3) (1,2,2)  (2,3) = 1+4+3+3+2 = 13

for _ in range(t_c):
    n = int(input())
    print(dp[n])
